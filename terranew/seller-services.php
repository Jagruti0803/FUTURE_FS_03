        document.addEventListener('DOMContentLoaded', function() {
            // Set minimum date for collection to tomorrow
            const today = new Date();
            const tomorrow = new Date(today);
            tomorrow.setDate(today.getDate() + 1);
            const minDate = tomorrow.toISOString().split('T')[0];
            document.getElementById('collectionDate').min = minDate;

            // Show the appropriate plastic info based on selection
            const plasticTypeRadios = document.querySelectorAll('.seller-plastic-radio');
            plasticTypeRadios.forEach(radio => {
                radio.addEventListener('change', function() {
                    document.querySelectorAll('.seller-plastic-info').forEach(info => {
                        info.style.display = 'none';
                    });
                    if (this.value === 'PET') {
                        document.getElementById('PETInfo').style.display = 'block';
                    } else if (this.value === 'HDPE') {
                        document.getElementById('HDPEInfo').style.display = 'block';
                    }
                });
            });

            // Initialize with PET info showing
            document.getElementById('PETInfo').style.display = 'block';

            // Image preview functionality
            const imageInput = document.getElementById('plasticImages');
            const imagePreview = document.getElementById('plasticImagePreview');
            
            imageInput.addEventListener('change', function() {
                if (this.files && this.files[0]) {
                    const reader = new FileReader();
                    reader.onload = function(e) {
                        imagePreview.style.display = 'block';
                        imagePreview.src = e.target.result;
                    }
                    reader.readAsDataURL(this.files[0]);
                }
            });

            // Form submission
            document.getElementById('recyclingForm').addEventListener('submit', function(e) {
                e.preventDefault();
                
                // In a real application, you would send this data to your server
                const formData = {
                    name: document.getElementById('sellerName').value,
                    phone: document.getElementById('sellerPhone').value,
                    address: document.getElementById('sellerAddress').value,
                    plasticType: document.querySelector('input[name="plasticType"]:checked').value,
                    quantity: document.getElementById('plasticQuantity').value,
                    unit: document.getElementById('quantityUnit').value,
                    collectionDate: document.getElementById('collectionDate').value,
                    notes: document.getElementById('additionalNotes').value
                };
                
                console.log('Form data:', formData);
                alert('Thank you for your recycling request! We will contact you shortly to arrange collection.');
                this.reset();
                imagePreview.style.display = 'none';
            });
        });
    </script>

    
